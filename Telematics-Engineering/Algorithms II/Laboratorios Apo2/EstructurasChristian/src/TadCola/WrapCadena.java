package TadCola;

public class WrapCadena implements Prioridad{
	
	private int prioridad;
	
	private String cadena;
	
	public WrapCadena(int prioridad, String cadena) {
		this.prioridad = prioridad;
		this.cadena = cadena;
	}


	public int getPrioridad() {
		return prioridad;
	}

	public void setPrioridad(int prioridad) {
		this.prioridad = prioridad;
	}

	@Override
	public String toString() {
		return cadena;
	}


}
